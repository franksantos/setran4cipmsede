    {
        //salvar as informações do modelo
        $modelos = new Modelo();
        $modelos->nome = Str::upper($request->input('nome'));
        $modelos->marca_id = $request->input('marca');
        $modelos->save();
        return redirect(route('modelos.index'));
    }

